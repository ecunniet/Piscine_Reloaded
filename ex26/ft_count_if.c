int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int ret;

	i = 0;
	ret = 0;
	while (tab[i])
	{
		if (f(tab[i]) == 1)
		{
			ret++;
		}
		i++;
	}
	return (ret);
}
